void	ft_ultimate_div_mod(int *a, int *b)
{
	int	tmp;

	tmp = *a;
	if (*a != 0 || *b != 0)
	{
		*a = *a / *b;
		*b = tmp % *b;
	}
	else
	{
		*a = 0;
		*b = 0;
	}
}
