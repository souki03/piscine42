#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr(nb / 10);
	ft_putchar(nb % 10 + '0');
}

int	*ft_rev(int view[4])
{
	int	i;
	int	temp;

	i = 0;
	while (i < 2)
	{
		temp = view[i];
		view[i] = view[3 - i];
		view[3 - i] = temp;
		i++;
	}
	return (view);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}
